function K = Gold(Nb, sinPhi, X)
    persistent goldValues nbAxis sinPhiAxis xAxis
    persistent nbAxisIdx sinPhiIdx xAxisIdx
     
    % X, SinPhi, NB
    if isempty(goldValues)
        goldTab = GoldData();               
        nbAxis     = sort(unique(goldTab(:,1)));
        sinPhiAxis = sort(unique(goldTab(:,2)));    
        xAxis      = sort(unique(goldTab(:,3))); 
        goldValues = reshape(goldTab(:, 4), [14, 31, 5]);
        [xAxisIdx, sinPhiIdx, nbAxisIdx] = ndgrid(xAxis, sinPhiAxis, nbAxis);
    end
    if sum(isnan(sinPhi))<length(sinPhi)
        sinPhi = min(sinPhi,0.95);
        K = interpn(xAxisIdx, sinPhiIdx, nbAxisIdx, goldValues, X, sinPhi, Nb);
    elseif sum(isnan(sinPhi))==length(sinPhi)
        K=ones(size(sinPhi));
    end
